   private void signup() {
	   char[] input = jPasswordField1.getPassword();
	   char[] input2 = jPasswordField2.getPassword();
       String password = String.copyValueOf(input);
       String password2 = String.copyValueOf(input2);
	   String username = jTextField1.getText(), ra = jTextField5.getText(), email = jTextField4.getText(), fn = jTextField2.getText(), ln = jTextField3.getText();
	   
	   if (username.length() == 0 || password.length() == 0 || ra.length() == 0 || email.length() == 0 || fn.length() == 0 || ln.length() == 0) {
		   MessageBox.error("Please fill in all the details", this);
		   return;
	   }
	   
	   if (!password.equals(password2)) {
		   MessageBox.error("Passwords not matched.", this);
		   return;
	   }
	   
	   try {
		Register.newUser(username, password2, email, fn, ln, ra);
		MessageBox.information("Done", this);
		this.dispose();
	} catch (CmsysException e) {
		MessageBox.error("Error.", this);
	}
   }
   
